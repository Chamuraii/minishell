#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (new == NULL)
		return ;
	if (*lst == NULL)
		ft_lstadd_front(lst, new);
	else
		ft_lstlast(*lst)-> next = new;
}
